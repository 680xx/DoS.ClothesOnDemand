public class CEO {

    // Attributes
    private int id;
    private String name;

    // Constructors
    public CEO() {
    }

    // Getter & Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
